/// This module contains constants for masking characters used in data sanitization.

/// Asterisk (*) is used as a masking character to replace sensitive data.
pub const MASK_ASTERISK: &str = "*";

/// Minus (-) is used as a masking character to replace sensitive data.
pub const MASK_MINUS: &str = "-";

/// Plus (+) is used as a masking character to replace sensitive data.
pub const MASK_PLUS: &str = "+";